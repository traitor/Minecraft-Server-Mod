/**
 * Location.java - Used for passing a location to other functions and such.
 * @author James
 */
public class Location {
    /**
     * X location
     */
    public double x;
    /**
     * Z location
     */
    public double z;
    /**
     * Y location
     */
    public double y;
    /**
     * Rotation
     */
    public float rotX;
    /**
     * Pitch
     */
    public float rotY;
}
